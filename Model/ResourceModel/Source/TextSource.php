namespace IgorRain\CodeGenerator\Model\ResourceModel\Source;

class TextSource extends AbstractSource
{
    /**
     * @var null|string
     */
    private $content;

    public function getContent(): ?string
    {
        return $this->content;
    }

    public function setContent(string $content): void
    {
        $this->content = $content;
    }
}
